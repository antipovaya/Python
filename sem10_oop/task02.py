class Rectangle:

    def __init__(self, *args):
        if len(args) == 1:
            self.__length = args[0]
            self.__width = args[0]
        elif len(args) == 2:
            self.__length = args[0]
            self.__width = args[1]

    def get_perimeter(self):
        return 2 * (self.__width + self.__length)

    def get_square(self):
        return self.__length * self.__width


